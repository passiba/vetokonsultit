    // LisaaMatkustajaListener
    class LisaaMatkustajaBtnListener implements ActionListener {         
        public void actionPerformed(ActionEvent e) {
            
            try {
                int matkustajiayhteesa = 0, lisamatkustajienLkm = Integer.parseInt(jTextField_matkustajienLkm.getText());
                bussiInstance.nouseeKyytiin(lisamatkustajienLkm);
                matkustajiayhteesa = bussiInstance.montakoIhmista();
                jTextField_matkustajienLkm.setText("0");

                jLabel_matkustajienlkm.setText(String.valueOf(matkustajiayhteesa));

            } catch (NumberFormatException ne) {
            }
                       
        }
    }//PoistaMatkustajaListener
    class PoistaMatkustajaBtnListener implements ActionListener {

        public void actionPerformed(ActionEvent e) {
            try {
                int matkustajiayhteesa = 0, poistuvienmatkustajienLkm = Integer.parseInt(jTextField_matkustajienLkm.getText());
                bussiInstance.hyppaaPois(poistuvienmatkustajienLkm);
                matkustajiayhteesa = bussiInstance.montakoIhmista();
                jTextField_matkustajienLkm.setText("0");

                jLabel_matkustajienlkm.setText(String.valueOf(matkustajiayhteesa));
            } catch (NumberFormatException ne) {
            }

        }
    }

